package com.rabbitmq.commons;

public class UserInfo {
	
	public int id=0;
	public String name="";
	public String birthday="";
	public int age=0;
	public UserInfo(int id, String name, String birthday, int age) {
		super();
		this.id = id;
		this.name = name;
		this.birthday = birthday;
		this.age = age;
	}
	
	

}
